package com.voyageone.synship.modelbean;

public class WaybillRouteBean {

	private String auto_no;
    private String id;
    private String mailno;
    private String orderid;
    private String acceptTime;
    private String acceptAddress;
    private String remark;
    private String opCode;
    private String carrier_alias_name;

    public String getAuto_no() {
        return auto_no;
    }

    public void setAuto_no(String auto_no) {
        this.auto_no = auto_no;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMailno() {
        return mailno;
    }

    public void setMailno(String mailno) {
        this.mailno = mailno;
    }

    public String getOrderid() {
        return orderid;
    }

    public void setOrderid(String orderid) {
        this.orderid = orderid;
    }

    public String getAcceptTime() {
        return acceptTime;
    }

    public void setAcceptTime(String acceptTime) {
        this.acceptTime = acceptTime;
    }

    public String getAcceptAddress() {
        return acceptAddress;
    }

    public void setAcceptAddress(String acceptAddress) {
        this.acceptAddress = acceptAddress;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getOpCode() {
        return opCode;
    }

    public void setOpCode(String opCode) {
        this.opCode = opCode;
    }

    public String getCarrier_alias_name() {
        return carrier_alias_name;
    }

    public void setCarrier_alias_name(String carrier_alias_name) {
        this.carrier_alias_name = carrier_alias_name;
    }
}
