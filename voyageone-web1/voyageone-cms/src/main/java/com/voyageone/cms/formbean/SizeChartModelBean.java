public class SizeChartModelBean {
    
	private String channelId;
	
	private String sizeChartModelId;
	
	private String sizeChartModelName;
	
	private String sizeChartModelUrl;

	public String getChannelId() {
		return channelId;
	}

	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}

	public String getSizeChartModelId() {
		return sizeChartModelId;
	}

	public void setSizeChartModelId(String sizeChartModelId) {
		this.sizeChartModelId = sizeChartModelId;
	}

	public String getSizeChartModelName() {
		return sizeChartModelName;
	}

	public void setSizeChartModelName(String sizeChartModelName) {
		this.sizeChartModelName = sizeChartModelName;
	}

	public String getSizeChartModelUrl() {
		return sizeChartModelUrl;
	}

	public void setSizeChartModelUrl(String sizeChartModelUrl) {
		this.sizeChartModelUrl = sizeChartModelUrl;
	}
	
	
}
