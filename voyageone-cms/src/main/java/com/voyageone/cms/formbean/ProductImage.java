package com.voyageone.cms.formbean;

public class ProductImage extends ProductBaseBean{
	
	private String imageTypeId;
	
	private String imageName;
	
	private String imageUrl;
	
	private int imageId;
	
	private String displayOrder;

	/**
	 * @return the imageTypeId
	 */
	public String getImageTypeId() {
		return imageTypeId;
	}

	/**
	 * @param imageTypeId the imageTypeId to set
	 */
	public void setImageTypeId(String imageTypeId) {
		this.imageTypeId = imageTypeId;
	}

	/**
	 * @return the imageName
	 */
	public String getImageName() {
		return imageName;
	}

	/**
	 * @param imageName the imageName to set
	 */
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}

	/**
	 * @return the imageUrl
	 */
	public String getImageUrl() {
		return imageUrl;
	}

	/**
	 * @param imageUrl the imageUrl to set
	 */
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	/**
	 * @return the displayOrder
	 */
	public String getDisplayOrder() {
		return displayOrder;
	}

	/**
	 * @param displayOrder the displayOrder to set
	 */
	public void setDisplayOrder(String displayOrder) {
		this.displayOrder = displayOrder;
	}

	/**
	 * @return the imageId
	 */
	public int getImageId() {
		return imageId;
	}

	/**
	 * @param imageId the imageId to set
	 */
	public void setImageId(int imageId) {
		this.imageId = imageId;
	}
	
	

}
